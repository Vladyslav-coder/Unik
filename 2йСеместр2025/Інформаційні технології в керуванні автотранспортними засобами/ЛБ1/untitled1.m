Fs = 10e3;
f = 0.5e3; 
T = 10e-3; 
t = 0:1/Fs:3*T;
s = sin(2*pi*f*t).*(sin(2*pi*f*t) > 0); 
stem(t, s); 
xlabel('Час (с)'); ylabel('Амплітуда');
title('Позитивні напівхвилі синусоїди');

